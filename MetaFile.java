
public class MetaFile {
	private String name;
	private int numberOfPages;
	private int pageSize;
	private int size;
	
	public MetaFile(String name, int numberOfPages, int pageSize, int size)
	{
		this.name = name;
		this.numberOfPages = numberOfPages;
		this.pageSize = pageSize;
		this.size = size;
	}
}
